export const getEvenNumbers = (numbers) => numbers.filter(number => number % 2 === 0);

export const getSquaredNumbers = (arr) => arr.map(item => item * item);

export const getOddNumbres = (arr) => arr.filter(arr % 2 === 1);

export const sum = (a, b) => a + b;


